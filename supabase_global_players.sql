    execute format('drop policy if exists "Authenticated staff can delete %s" on public.%I', table_name, table_name);
    execute format('create policy "Authenticated staff can delete %s" on public.%I for delete to authenticated using (true)', table_name, table_name);
  end loop;
end $$;

