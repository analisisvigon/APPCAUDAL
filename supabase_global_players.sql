create or replace function public.save_global_player_profile(
  p_player jsonb,
  p_positions jsonb default '[]'::jsonb,
  p_sources jsonb default '[]'::jsonb,
  p_traits jsonb default '[]'::jsonb,
  p_membership jsonb default null
)
returns uuid
