-- Base global de futbolistas y migración reversible desde jugadores_rivales.
-- Ejecutar el archivo completo en Supabase SQL Editor. No elimina ninguna tabla ni columna legacy.
begin;

create table if not exists public.players_database (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  photo_url text,
  dob date,
  age text,
  height text,
  foot text,
  scouting_summary varchar(500),
  card_alert boolean not null default false,
  sent_off_alert boolean not null default false,
  suspended_alert boolean not null default false,
  injured_alert boolean not null default false,
  external_source text,
  external_player_id text,
  field_sources jsonb not null default '{}'::jsonb,
  legacy_origin_id uuid unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.players_database add column if not exists age text;

create unique index if not exists players_database_external_identity_uidx
on public.players_database (lower(external_source), external_player_id)
where external_source is not null and external_player_id is not null;

create index if not exists players_database_name_idx
on public.players_database (lower(name));

create table if not exists public.player_team_memberships (
  id uuid primary key default gen_random_uuid(),
  player_id uuid not null references public.players_database(id) on delete cascade,
  team_id uuid not null references public.equipos_rivales(id) on delete cascade,
  season text,
  start_date date,
  end_date date,
  is_current boolean not null default true,
  number text,
  captain boolean not null default false,
  is_key boolean not null default false,
  observed boolean not null default false,
  squad_role text,
  tactical_role text,
  tactical_slot integer,
  tactical_reserve_slot integer,
  source_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint player_team_memberships_role_check check (tactical_role is null or tactical_role in ('Titular', 'Reserva')),
  constraint player_team_memberships_slot_check check (tactical_slot is null or tactical_slot between 0 and 10),
  constraint player_team_memberships_reserve_slot_check check (tactical_reserve_slot is null or tactical_reserve_slot between 0 and 1),
  constraint player_team_memberships_placement_check check (
    (tactical_role is null and tactical_slot is null and tactical_reserve_slot is null)
    or (tactical_role = 'Titular' and tactical_slot is not null and tactical_reserve_slot is null)
    or (tactical_role = 'Reserva' and tactical_slot is not null and tactical_reserve_slot is not null)
  )
);

create unique index if not exists player_team_memberships_current_stage_uidx
on public.player_team_memberships (player_id, team_id)
where is_current;

create index if not exists player_team_memberships_team_current_idx
on public.player_team_memberships (team_id, is_current);

create unique index if not exists player_team_memberships_starter_slot_uidx
on public.player_team_memberships (team_id, tactical_slot)
where is_current and tactical_role = 'Titular' and tactical_slot is not null;

create unique index if not exists player_team_memberships_reserve_slot_uidx
on public.player_team_memberships (team_id, tactical_slot, tactical_reserve_slot)
where is_current and tactical_role = 'Reserva' and tactical_slot is not null and tactical_reserve_slot is not null;

create table if not exists public.player_positions (
  id bigint generated by default as identity primary key,
  player_id uuid not null references public.players_database(id) on delete cascade,
  position_type text not null check (position_type in ('natural', 'specific')),
  position_key text not null,
  is_primary boolean not null default false,
  source text not null default 'manual',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint player_positions_key_check check (
    (position_type = 'natural' and position_key in ('goalkeeper', 'defender', 'midfielder', 'forward'))
    or (position_type = 'specific' and position_key in (
      'goalkeeper', 'sweeper_keeper', 'right_back', 'left_back', 'right_wing_back', 'left_wing_back',
      'centre_back', 'right_centre_back', 'left_centre_back', 'libero', 'holding_midfield',
      'defensive_midfield', 'central_midfield', 'right_central_midfield', 'left_central_midfield',
      'attacking_midfield', 'right_midfield', 'left_midfield', 'right_winger', 'left_winger',
      'second_striker', 'centre_forward', 'mobile_forward', 'target_forward'
    ))
  ),
  unique (player_id, position_type, position_key)
);

create unique index if not exists player_positions_one_primary_per_type_uidx
on public.player_positions (player_id, position_type)
where is_primary;

create index if not exists player_positions_key_idx
on public.player_positions (position_type, position_key, player_id);

create table if not exists public.player_sources (
  id bigint generated by default as identity primary key,
  player_id uuid not null references public.players_database(id) on delete cascade,
  url text not null,
  source_name text not null default 'Otro',
  is_primary boolean not null default false,
  created_at timestamptz not null default now(),
  unique (player_id, url)
);

create unique index if not exists player_sources_one_primary_uidx
on public.player_sources (player_id)
where is_primary;

create table if not exists public.player_scouting_traits (
  id bigint generated by default as identity primary key,
  player_id uuid not null references public.players_database(id) on delete cascade,
  category text not null check (category in ('strength', 'vulnerability', 'trend')),
  label text not null,
  position_family text,
  created_at timestamptz not null default now(),
  unique (player_id, category, label)
);

create table if not exists public.legacy_rival_player_migration (
  legacy_player_id uuid primary key references public.jugadores_rivales(id) on delete cascade,
  global_player_id uuid not null references public.players_database(id) on delete cascade,
  membership_id uuid references public.player_team_memberships(id) on delete set null,
  match_basis text not null default 'legacy_row',
  review_status text not null default 'pending' check (review_status in ('pending', 'confirmed', 'possible_duplicate')),
  created_at timestamptz not null default now()
);

