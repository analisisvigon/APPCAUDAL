-- Migración conservadora: un perfil por fila legacy. Nunca une automáticamente solo por nombre.
insert into public.players_database (
  name, photo_url, dob, age, height, foot, scouting_summary,
  card_alert, sent_off_alert, suspended_alert, injured_alert,
  external_source, external_player_id, field_sources, legacy_origin_id
)
select distinct on (candidate.identity_key)
  candidate.name, candidate.image, candidate.dob, candidate.age, candidate.height, candidate.foot, left(candidate.notes, 500),
  coalesce(candidate.card_alert, candidate.yellow_risk, false), coalesce(candidate.sent_off_alert, false),
  coalesce(candidate.suspended_alert, candidate.suspended, false), coalesce(candidate.injured_alert, candidate.injured, false),
  nullif(trim(candidate.external_source), ''), nullif(trim(candidate.external_player_id), ''), coalesce(candidate.field_sources, '{}'::jsonb), candidate.id
from (
  select jr.*,
    case when nullif(trim(jr.external_source), '') is not null and nullif(trim(jr.external_player_id), '') is not null
      then 'external:' || lower(trim(jr.external_source)) || ':' || trim(jr.external_player_id)
      else 'legacy:' || jr.id::text
    end as identity_key
  from public.jugadores_rivales jr
) candidate
where not exists (
  select 1 from public.players_database gp
  where gp.legacy_origin_id = candidate.id
     or (
       nullif(trim(candidate.external_source), '') is not null and nullif(trim(candidate.external_player_id), '') is not null
       and lower(gp.external_source) = lower(trim(candidate.external_source))
       and gp.external_player_id = trim(candidate.external_player_id)
     )
)
order by candidate.identity_key, candidate.id;

insert into public.legacy_rival_player_migration (legacy_player_id, global_player_id, match_basis)
select jr.id, matched.id,
  case when matched.legacy_origin_id = jr.id then 'legacy_row' else 'external_id' end
from public.jugadores_rivales jr
join lateral (
  select gp.id, gp.legacy_origin_id
  from public.players_database gp
  where gp.legacy_origin_id = jr.id
     or (
       nullif(trim(jr.external_source), '') is not null and nullif(trim(jr.external_player_id), '') is not null
       and lower(gp.external_source) = lower(trim(jr.external_source))
       and gp.external_player_id = trim(jr.external_player_id)
     )
  order by (gp.legacy_origin_id = jr.id) desc
  limit 1
) matched on true
on conflict (legacy_player_id) do update
set global_player_id = excluded.global_player_id, match_basis = excluded.match_basis;

insert into public.player_team_memberships (
  player_id, team_id, is_current, number, captain, is_key, observed, squad_role,
  tactical_role, tactical_slot, tactical_reserve_slot, source_url
)
select distinct on (migration.global_player_id, jr.equipo_rival_id, coalesce(jr.active_in_squad, true))
  migration.global_player_id, jr.equipo_rival_id, coalesce(jr.active_in_squad, true), jr.number,
  coalesce(jr.captain, false), coalesce(jr.is_key, false), coalesce(jr.observed, false), jr.role,
  jr.tactical_role, jr.tactical_slot, jr.tactical_reserve_slot, jr.source_profile_url
from public.jugadores_rivales jr
join public.legacy_rival_player_migration migration on migration.legacy_player_id = jr.id
where not exists (
  select 1 from public.player_team_memberships membership
  where membership.player_id = migration.global_player_id and membership.team_id = jr.equipo_rival_id and membership.is_current = coalesce(jr.active_in_squad, true)
)
order by migration.global_player_id, jr.equipo_rival_id, coalesce(jr.active_in_squad, true) desc, jr.id;

insert into public.legacy_rival_player_migration (legacy_player_id, global_player_id, membership_id)
select jr.id, migration.global_player_id, membership.id
from public.jugadores_rivales jr
join public.legacy_rival_player_migration migration on migration.legacy_player_id = jr.id
left join lateral (
  select item.id from public.player_team_memberships item
  where item.player_id = migration.global_player_id and item.team_id = jr.equipo_rival_id
  order by item.is_current desc, item.created_at desc limit 1
) membership on true
on conflict (legacy_player_id) do update
set global_player_id = excluded.global_player_id, membership_id = excluded.membership_id;

update public.jugadores_rivales jr
set global_player_id = migration.global_player_id,
    membership_id = migration.membership_id
from public.legacy_rival_player_migration migration
where migration.legacy_player_id = jr.id
  and (jr.global_player_id is distinct from migration.global_player_id or jr.membership_id is distinct from migration.membership_id);

