security invoker
set search_path = public
as $$
declare
  global_id uuid;
  membership_id uuid;
begin
  global_id := nullif(p_player ->> 'id', '')::uuid;
  if global_id is null then
    insert into public.players_database (
      name, photo_url, dob, age, height, foot, scouting_summary,
      card_alert, sent_off_alert, suspended_alert, injured_alert,
      external_source, external_player_id, field_sources
    ) values (
      trim(p_player ->> 'name'), nullif(p_player ->> 'photoUrl', ''), nullif(p_player ->> 'dob', '')::date, nullif(p_player ->> 'age', ''),
      nullif(p_player ->> 'height', ''), nullif(p_player ->> 'foot', ''), left(nullif(p_player ->> 'scoutingSummary', ''), 500),
      coalesce((p_player ->> 'cardAlert')::boolean, false), coalesce((p_player ->> 'sentOffAlert')::boolean, false),
      coalesce((p_player ->> 'suspendedAlert')::boolean, false), coalesce((p_player ->> 'injuredAlert')::boolean, false),
      nullif(p_player ->> 'externalSource', ''), nullif(p_player ->> 'externalPlayerId', ''), coalesce(p_player -> 'fieldSources', '{}'::jsonb)
    ) returning id into global_id;
  else
    update public.players_database set
      name = trim(p_player ->> 'name'),
      photo_url = nullif(p_player ->> 'photoUrl', ''),
      dob = nullif(p_player ->> 'dob', '')::date,
      age = nullif(p_player ->> 'age', ''),
      height = nullif(p_player ->> 'height', ''),
      foot = nullif(p_player ->> 'foot', ''),
      scouting_summary = left(nullif(p_player ->> 'scoutingSummary', ''), 500),
      card_alert = coalesce((p_player ->> 'cardAlert')::boolean, false),
      sent_off_alert = coalesce((p_player ->> 'sentOffAlert')::boolean, false),
      suspended_alert = coalesce((p_player ->> 'suspendedAlert')::boolean, false),
      injured_alert = coalesce((p_player ->> 'injuredAlert')::boolean, false),
      external_source = nullif(p_player ->> 'externalSource', ''),
      external_player_id = nullif(p_player ->> 'externalPlayerId', ''),
      field_sources = coalesce(p_player -> 'fieldSources', field_sources)
    where id = global_id;
    if not found then raise exception 'Jugador global no encontrado'; end if;
  end if;

  delete from public.player_positions where player_id = global_id;
  insert into public.player_positions (player_id, position_type, position_key, is_primary, source)
  select global_id, item.position_type, item.position_key, item.is_primary, coalesce(nullif(item.source, ''), 'manual')
  from jsonb_to_recordset(coalesce(p_positions, '[]'::jsonb))
    as item(position_type text, position_key text, is_primary boolean, source text);

  delete from public.player_sources where player_id = global_id;
  insert into public.player_sources (player_id, url, source_name, is_primary)
  select global_id, item.url, coalesce(nullif(item.source_name, ''), 'Otro'), item.is_primary
  from jsonb_to_recordset(coalesce(p_sources, '[]'::jsonb))
    as item(url text, source_name text, is_primary boolean)
  where nullif(trim(item.url), '') is not null;

  delete from public.player_scouting_traits where player_id = global_id;
  insert into public.player_scouting_traits (player_id, category, label, position_family)
  select global_id, item.category, trim(item.label), nullif(item.position_family, '')
  from jsonb_to_recordset(coalesce(p_traits, '[]'::jsonb))
    as item(category text, label text, position_family text)
  where nullif(trim(item.label), '') is not null;

  if p_membership is not null and nullif(p_membership ->> 'teamId', '') is not null then
    membership_id := nullif(p_membership ->> 'id', '')::uuid;
    if membership_id is null then
      select id into membership_id from public.player_team_memberships
      where player_id = global_id and team_id = (p_membership ->> 'teamId')::uuid and is_current
      order by created_at desc limit 1;
    end if;
    if membership_id is null then
      if coalesce(nullif(p_membership ->> 'mode', ''), 'replace') = 'replace' then
        update public.player_team_memberships
        set is_current = false, end_date = coalesce(end_date, current_date)
        where player_id = global_id and is_current and team_id <> (p_membership ->> 'teamId')::uuid;
      end if;
      insert into public.player_team_memberships (
        player_id, team_id, season, start_date, is_current, number, captain, is_key, observed, source_url
      ) values (
        global_id, (p_membership ->> 'teamId')::uuid, nullif(p_membership ->> 'season', ''),
        nullif(p_membership ->> 'startDate', '')::date, true, nullif(p_membership ->> 'number', ''),
        coalesce((p_membership ->> 'captain')::boolean, false), coalesce((p_membership ->> 'isKey')::boolean, false),
        coalesce((p_membership ->> 'observed')::boolean, false), nullif(p_membership ->> 'sourceUrl', '')
      );
    else
      update public.player_team_memberships set
        season = nullif(p_membership ->> 'season', ''), number = nullif(p_membership ->> 'number', ''),
        captain = coalesce((p_membership ->> 'captain')::boolean, false),
        is_key = coalesce((p_membership ->> 'isKey')::boolean, false),
        observed = coalesce((p_membership ->> 'observed')::boolean, false)
      where id = membership_id and player_id = global_id;
    end if;
  end if;
  return global_id;
end $$;

create or replace function public.assign_global_player_to_team(
  p_player_id uuid,
  p_team_id uuid,
  p_mode text default 'replace',
  p_season text default null,
  p_start_date date default current_date
)
returns uuid
language plpgsql
security invoker
set search_path = public
as $$
declare membership_id uuid;
begin
  select id into membership_id from public.player_team_memberships
  where player_id = p_player_id and team_id = p_team_id and is_current
  order by created_at desc limit 1;
  if membership_id is not null then return membership_id; end if;
  if p_mode = 'replace' then
    update public.player_team_memberships
    set is_current = false, end_date = coalesce(end_date, p_start_date)
    where player_id = p_player_id and is_current;
  elsif p_mode <> 'keep_both' then
    raise exception 'Modo de afiliación no válido: %', p_mode;
  end if;
  insert into public.player_team_memberships (player_id, team_id, season, start_date, is_current)
  values (p_player_id, p_team_id, p_season, p_start_date, true)
  returning id into membership_id;
  return membership_id;
end $$;

commit;

